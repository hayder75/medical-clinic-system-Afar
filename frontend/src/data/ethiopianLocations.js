// Get all woredas as a flat array for search
export const getAllWoredas = () => {
  const woredas = [];
  Object.values(ETHIOPIAN_WOREDAS).forEach(regionWoredas => {
    woredas.push(...regionWoredas);
  });
  return woredas;
};

// Get zones for a specific region
export const getZonesForRegion = (regionId) => {
  return ETHIOPIAN_ZONES[regionId] || [];
};

// Get woredas for a specific zone
export const getWoredasForZone = (zoneId) => {
  return ETHIOPIAN_WOREDAS[zoneId] || [];
};
